package com.ilife.analyzer.serializer;

public class Link {
	String web;
	String wap;
	String qrcode;
	String web2;
	String wap2;
	String token;
	public String getWap() {
		return wap;
	}
	public void setWap(String wap) {
		this.wap = wap;
	}
	public String getQrcode() {
		return qrcode;
	}
	public void setQrcode(String qrcode) {
		this.qrcode = qrcode;
	}
	public String getWeb2() {
		return web2;
	}
	public void setWeb2(String web2) {
		this.web2 = web2;
	}
	public String getWap2() {
		return wap2;
	}
	public void setWap2(String wap2) {
		this.wap2 = wap2;
	}
	public String getWeb() {
		return web;
	}
	public void setWeb(String web) {
		this.web = web;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
}
