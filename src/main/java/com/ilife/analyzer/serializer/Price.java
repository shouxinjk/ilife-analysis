package com.ilife.analyzer.serializer;

public class Price {
	double take;
	double sale;
	double bid;
	public double getTake() {
		return take;
	}
	public void setTake(double take) {
		this.take = take;
	}
	public double getSale() {
		return sale;
	}
	public void setSale(double sale) {
		this.sale = sale;
	}
	public double getBid() {
		return bid;
	}
	public void setBid(double bid) {
		this.bid = bid;
	}
}
